    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem().toString().equals("Lütfen Seçiniz")==false && jComboBox2.getSelectedItem() !=null && jComboBox3.getSelectedItem() != null && jDateChooser1.getDate()!=null)
        {
        _Grup_21_Mysql_Baglanti baglanti = new _Grup_21_Mysql_Baglanti();
        Connection baglan = baglanti.baglan();
        String kan_turu = jComboBox2.getSelectedItem().toString();
        String bagis_yeri = jComboBox3.getSelectedItem().toString();
        String id=jComboBox1.getSelectedItem().toString();
        id=id.substring(0,2);
        id=id.trim();
        if(id.length()==1)
        {
            id=jComboBox1.getSelectedItem().toString();
            id=id.substring(0,1);
            
        }
            
        String bagis_tarih = jDateChooser1.getDate().toString();
        boolean flag=false;  
        int sonuc;
        
        try {
            Statement stat = baglan.createStatement();
            String sql ="UPDATE _grup_21_kan_bagis_randevu SET kan_turu='"+kan_turu+"' , bagis_yeri='"+bagis_yeri+"', bagis_tarih='"+bagis_tarih+"' WHERE (id = '"+id+"')";
            sonuc=stat.executeUpdate(sql);
            if(sonuc==1)
                flag=true;
        }catch(SQLException ex) {
            Logger.getLogger(_Grup_21_KanBagis_Randevuİptal.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(flag==true)
        {
          JOptionPane.showMessageDialog(null, "Randevunuz Başarıyla Güncellenmiştir");
          this.setVisible(false);
          _Grup_21_Kullanici_Anasayfa kanasayfa = new _Grup_21_Kullanici_Anasayfa();
          kanasayfa.setVisible(true);
        }
            
            }
        else
            JOptionPane.showMessageDialog(null, "Lütfen Seçenekleri Doldurunuz");
    }//GEN-LAST:event_jButton4ActionPerformed

