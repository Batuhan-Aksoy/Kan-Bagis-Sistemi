    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedItem().toString().equals("Lütfen Seçiniz")==false)
        {
        _Grup_21_Mysql_Baglanti baglanti = new _Grup_21_Mysql_Baglanti();
        Connection baglan = baglanti.baglan();
        boolean flag = false;
        int sonuc;
        try {
            Statement stat2 = baglan.createStatement();
            String id = jComboBox1.getSelectedItem().toString();
            id=id.substring(0,3);
            id=id.trim();
            String sql2 = "DELETE FROM _grup_21_kan_bagis_randevu WHERE (id = '" + id + "')";
            sonuc = stat2.executeUpdate(sql2);
            if (sonuc == 1) {
                flag = true;
            }

        } catch (SQLException ex) {
            Logger.getLogger(_Grup_21_KanBagis_Randevuİptal.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (flag == true) {
            JOptionPane.showMessageDialog(null, "Randevunuz İptal Edilmiştir");
            this.setVisible(false);
            _Grup_21_Kullanici_Anasayfa kansayfa = new _Grup_21_Kullanici_Anasayfa();
            kansayfa.setVisible(true);
        }
        else
            JOptionPane.showMessageDialog(null, "Randevunuz İptal Edilmedi");
        }
        else
            JOptionPane.showMessageDialog(null, "Lütfen silmek istediğiniz randevunuzu seçiniz");
    }//GEN-LAST:event_jButton1ActionPerformed

